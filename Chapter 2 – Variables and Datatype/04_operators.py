# Arthemetic operator: +, -, *
a = 2
b = 2
c = a + b
print(c)

# Assignment operators: =, +=, -=
a = 4-2 #assigned 4-2 in a
print (a)

b =3
b += 3 # Increment the value of b by 3 and assign it to b
print(b)
b -= 3 # decrement the value of b by 3 and assign it to b
print(b)

# Comparison operators: ==, >, >=, <, !=  (this operator always return TRUE or FALSE i.e. boolean)
e = 5>=5
print(e)
d = 5>5
print(d)


